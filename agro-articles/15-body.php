<h3>Уход за горохом</h3>
<p>
После появления всходов необходимо разрыхлить междурядье. При слабом развитии растения можно подкормить минеральными удобрениями из расчета 10 г на метр квадратный. В засушливую погоду требуется полив из расчета 10 литров на метр квадратный.
</p>
<h3>Уборка и хранение</h3>
<p>
Убирают горошек по мере созревания, выборочно, обрывая бобы с зернами 7-8 мм в диаметре. Бобы хранятся всего 10-12 часов, но в холодильнике могут сохранятся в течение недели.
</p>