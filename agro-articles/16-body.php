<h3>Уход за салатом</h3>
<p>
Уход за салатом заключается в рыхлении, прополке и поливах. Салат, выращиваемый из семян, дважды прореживают, оставляя сначала 5-7, а затем 20-30 см между растениями. Поливают салат по мере необходимости, избегая попадания воды на листья.
</p>
<h3>Уборка салата</h3>
<p>
Листовой салат убирают примерно через месяц после появления всходов. Полная уборка производится при образовании 8-9 листьев в розетке. Передержка растения в грунте ведет к накоплению горечи в листьях. У кочанных сортов спелость наступает через 1,5-2,5 месяца, у салата ромэн - через 2,5-3 месяца. Листья спаржевого салата убираются когда их образуется по 15-20 штук, при этом вегетационный период спаржевого салата составляет 2,5-3 месяца.
</p>
<h3>Хранение салата</h3>
<p>
Салат хранится  в холодильнике кочанный в течение 2-3 недель, спаржевый — 1 месяц, ромен- 2-3 месяца. Выкопанное с корнем растения спаржевого салата могут длительно сохраняться в темных холодных помещениях.
</p>