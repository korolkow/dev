<h2>Методы, способы выращивания тыквы</h2>
<p>
<h3>Подготовка почвы и посев</h3>
Под тыкву рекомендуется отводить хорошо прогреваемые участки с 
плодородными почвами нейтральной кислотности, богатые перегноем. 
Рекомендуется вносить повышенные дозы органических удобрений.
Весной почва рыхлится на глубину 25-35 см. Если мощность плодородного 
слоя недостаточна, его необходимо увеличить, внося органические 
удобрения.   Так как тыква высаживается поздно, то отведенный под нее 
участок можно предварительно засеять редисом, салатом или луком. Кроме 
органических, в нечерноземной зоне также рекомендуется вносить 
минеральные удобрения из расчета 100-150 грамм на метр квадратный.
Для посева используют более крупные семена, которые предварительно 
рекомендуется прорастить во влажной ткани. Так как вегетационный период 
тыквы 4-5 месяцев, то ее целесообразно выращивать при помощи рассады.
Рассаду тыквы можно подготовить в домашних условиях на подоконнике или 
в парниках. Семена тыквы высевают по одному в горшочек , диаметром 15 
см, примерно за три недели до посадки в грунт.  Поливают растение 
умеренно и нечасто. Возможно внесение минеральных и органических 
удобрений каждые 10 суток.  Высадка рассады в открытый грунт начинается 
при установлении устойчивой теплой погоды. Поле посадки рассаду хорошо 
поливают.
</p>
<p>
Тыкву можно выращивать, высевая семена в грунт: крупноплодную и 
твердокорую при температуре почвы от +10 градусов и при температуре 
воздуха от +15 градусов, а мускатную — при температуре почвы от +12, а 
воздуха от +17 градусов. Семена заделывают на глубину около 10 см, на 
тяжелых почвах - около 5 см. Семена высевают в лунки по две-три штуки.
Наибольшую опасность для всходов тыквы представляют возвраты холодов. 
Для защиты от заморозков используют вечерний полив, укрытие пленкой, 
бумажными колпаками, мешковиной и другими материалами, поверх специально 
подготовленных каркасов.
</p>
<h3>Уход за тыквой</h3>
<p>После посадки семян, почву регулярно рыхлят на глубину до 7 см. Тыква 
отзывчива на все виды удобрений. Подкормки следует производить через 10 
суток после посадки рассады или через три недели после посева семян. 
Кроме внесения удобрения в грунт, возможны подкормки путем опрыскивания 
листьев раствором минеральных удобрений из расчета 10 г на 10 л 
воды. Тыква требует обильного полива. Оптимальная влажность почвы должна 
быть не ниже 70%.
</p>
<h3>Сбор и хранение</h3>
<p>
Сбор плодов начинают до наступления заморозков. Признак спелости плодов 
— усыхание плодоножки и ее затвердение.  Созревшие здоровые плоды 
прогревают на солнце в течении 10 суток. Хранят тыкву в затененных помещениях.
</p>