<h1>Выращивание тыквы</h1>
<img src="pictures/pumpkin1.jpg" style="width:400px; float:left; margin: 8px 8px 8px 0;"/>
<p>
Тыква возделывается более семи тысяч лет, причем родиной ее считается 
центральная и южная Америка. В Европе культура появилась с середины 16 
века, а в наше время выращивается практически на всех континентах. В 
России распространено три вида тыквы: крупноплодная, твердокорая и 
мускатная (возделывается в основном на юге). Крупноплодная тыква 
является самой холодостойкой, хорошо растет в средней полосе и на юге 
России. Плоды могут достигать 90 и более килограммов, и при этом хорошо 
сохраняются до нового урожая. Твердокорая тыква отличается 
скороспелостью. Плоды обычно яйцевидной формы, с желтой или оранжевой 
окраской и полосатым рисунком. Мускатная тыква самая теплолюбивая и 
позднеспелая, но дает плоды самого высокого качества.
</p>
<p>
Тыква обладает сильно развитой корневой системой, главный корень может 
проникать в почву на глубину до 3-х метров, а по горизонтали — до 5-ти 
метров. Опыление тыквы осуществляется только насекомыми, в основном 
пчелами, Плод формируется в течение одного-двух месяцев и достигает 5-10 
кг.
</p>
<p>
Их всех бахчевых культур тыква наименее требовательна к теплу. Семена 
большинства видов прорастают при температуре +11..+13 градусов, мускатной — при +12..+14.  
Оптимальная температура для нормального роста 
и развития растения не менее +20 градусов. При снижении температуры до 
+13 градусов рост растения замедляется, а урожайность и качество плодов 
резко снижаются. Тыква требовательна к свету, сразу после появления, а 
также в период цветения и созревания плодов. Растение нуждается в 
большом количестве влаги. При недостатке влаги возможно опадание 
завязей.
</p>
