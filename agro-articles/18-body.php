<h2>Уход и уборка</h2>
<p>
Уход за укропом заключается в рыхлении и прополке, а также поливах в сухую погоду. Зелень укропа убирается примерно через месяц после посева, когда высота растения достигнет 20 см, а соцветия с семенами для засола — через два месяца после посева.
</p>