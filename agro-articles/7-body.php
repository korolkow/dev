<h2>Выращивание дайкона</h2>
<p>
Дайкон — растение семейства капустных, родственное редьке и редису.  Корнеплоды обладают высокими вкусовыми качествами и лишены острого вкуса. Масса корнеплодов достигает 2-3 кг. Дайкон распространен в Юго-Восточной Азии, особенно в Японии.
</p>
<p>
Дайкон — двухлетние растение, на второй год формирует розетку листьев и цветонос. Форма корнеплодов может быть цилиндрической, конической, овальной или округлой. Поверхность корнеплодов гладкая.
</p>
<p>
Дайкон холодостойкое растение, минимальная температура прорастания  семян +5 градусов, всходы переносят заморозки до -3, оптимальная температура для роста +20..+25 градусов. Для выращивания подходят рыхлые почвы со слабокислой реакцией.
</p>